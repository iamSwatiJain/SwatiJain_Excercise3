using System;

namespace SwatiJain_Excercise3
{
    class Person
    {
        private int age;
        private String fName;
        private String lName;
        private double height;
        private double weight;

        public int Age
        {
            get
            {
                return age;
            }
            set
            {
                age = value;
            }
        }

        public String FName
        {
            get
            {
                return fName;
            }
            set
            {
                fName = value;
            }
        }

        public String LName
        {
            get
            {
                return lName;
            }
            set
            {
                lName = value;
            }
        }

        public double Height
        {
            get
            {
                return height;
            }
            set
            {
                height = value;
            }
        }

        public double Weight
        {
            get
            {
                return weight;
            }
            set
            {
                weight = value;
            }
        }

    }
}
